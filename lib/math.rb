

def addition(num1, num2)
new_num = num1 + num2
return new_num
end

def subtraction(num1, num2)
new_sub_num = num1 - num2
return new_sub_num
end

def division(num1, num2)
div_num = num1 / num2
return div_num
end

def multiplication(num1, num2)
multi_num = num1 * num2
return multi_num
end

def modulo(num1, num2)
mod_num = num1 % num2
return mod_num
end

def square_root(num)
sqr_num = Math.sqrt(num)
return sqr_num
end
