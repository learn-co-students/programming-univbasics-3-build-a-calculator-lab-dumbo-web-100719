def addition(num1, num2)
  sum = num1 + num2
end

def subtraction(num1, num2)
  subtract = num1 - num2
end

def division(num1, num2)
  division = num1 / num2
end

def multiplication(num1, num2)
  multiplication = num1 * num2
end

def modulo(num1, num2)
  division = num1 % num2
end

def square_root(num)
  Integer.sqrt(num)
end
