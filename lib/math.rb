def addition(num1, num2)
  sum = num1 + num2
  return sum
end

def subtraction(num1, num2)
  sub = num1 - num2
  return sub
end

def division(num1, num2)
  div = num1 / num2
  return div
end

def multiplication(num1, num2)
  product = num1 * num2
  return product
end

def modulo(num1, num2)
  mods = num1 % num2
  return mods
end

def square_root(num)
  sqrtt = Math.sqrt(num)
  return sqrtt
end
